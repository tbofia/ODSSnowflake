CREATE OR REPLACE VIEW dbo.Drugs
AS

SELECT 
		ODSPOSTINGGROUPAUDITID
		,ODSCUSTOMERID
		,ODSCREATEDATE
		,ODSSNAPSHOTDATE
		,ODSROWISCURRENT
		,ODSHASHBYTESVALUE
		,DMLOPERATION
		,DRUGCODE
		,DRUGSDESCRIPTION
		,DISP
		,DRUGTYPE
		,CAT
		,UPDATEFLAG
		,UV
		,CREATEDATE
		,CREATEUSERID
		,MODDATE
		,MODUSERID
FROM src.Drugs
WHERE   ODSROWISCURRENT = 1
	AND DMLOPERATION <> 'D';


