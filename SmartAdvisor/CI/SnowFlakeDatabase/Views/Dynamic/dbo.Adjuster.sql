CREATE OR REPLACE VIEW dbo.Adjuster
AS

SELECT 
		ODSPOSTINGGROUPAUDITID
		,ODSCUSTOMERID
		,ODSCREATEDATE
		,ODSSNAPSHOTDATE
		,ODSROWISCURRENT
		,ODSHASHBYTESVALUE
		,DMLOPERATION
		,CLAIMSYSSUBSET
		,ADJUSTER
		,FIRSTNAME
		,LASTNAME
		,MINITIAL
		,TITLE
		,ADDRESS1
		,ADDRESS2
		,CITY
		,STATE
		,ZIP
		,PHONENUM
		,PHONENUMEXT
		,FAXNUM
		,EMAIL
		,CREATEDATE
		,CREATEUSERID
		,MODDATE
		,MODUSERID
FROM src.Adjuster
WHERE   ODSROWISCURRENT = 1
	AND DMLOPERATION <> 'D';


