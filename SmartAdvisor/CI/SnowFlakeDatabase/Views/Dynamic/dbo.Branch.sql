CREATE OR REPLACE VIEW dbo.Branch
AS

SELECT 
		ODSPOSTINGGROUPAUDITID
		,ODSCUSTOMERID
		,ODSCREATEDATE
		,ODSSNAPSHOTDATE
		,ODSROWISCURRENT
		,ODSHASHBYTESVALUE
		,DMLOPERATION
		,CLAIMSYSSUBSET
		,BRANCHSEQ
		,NAME
		,EXTERNALID
		,BRANCHID
		,LOCATIONCODE
		,ADMINKEY
		,ADDRESS1
		,ADDRESS2
		,CITY
		,STATE
		,ZIP
		,PHONENUM
		,FAXNUM
		,CONTACTNAME
		,TIN
		,STATETAXID
		,DIRNUM
		,MODUSERID
		,MODDATE
		,RULEFIRE
		,FEERATECNTRLEX
		,FEERATECNTRLIN
		,SALESTAXEXEMPT
		,EFFECTIVEDATE
		,TERMINATIONDATE
FROM src.Branch
WHERE   ODSROWISCURRENT = 1
	AND DMLOPERATION <> 'D';


