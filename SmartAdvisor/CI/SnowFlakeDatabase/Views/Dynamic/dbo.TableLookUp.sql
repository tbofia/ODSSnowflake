CREATE OR REPLACE VIEW dbo.TableLookUp
AS

SELECT 
		ODSPOSTINGGROUPAUDITID
		,ODSCUSTOMERID
		,ODSCREATEDATE
		,ODSSNAPSHOTDATE
		,ODSROWISCURRENT
		,ODSHASHBYTESVALUE
		,DMLOPERATION
		,TABLECODE
		,TYPECODE
		,CODE
		,SITECODE
		,OLDCODE
		,SHORTDESC
		,SOURCE
		,PRIORITY
		,LONGDESC
		,OWNERAPP
		,RECORDSTATUS
		,CREATEDATE
		,CREATEUSERID
		,MODDATE
		,MODUSERID
FROM src.TableLookUp
WHERE   ODSROWISCURRENT = 1
	AND DMLOPERATION <> 'D';


