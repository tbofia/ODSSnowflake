CREATE OR REPLACE VIEW dbo.Insurer
AS

SELECT 
		ODSPOSTINGGROUPAUDITID
		,ODSCUSTOMERID
		,ODSCREATEDATE
		,ODSSNAPSHOTDATE
		,ODSROWISCURRENT
		,ODSHASHBYTESVALUE
		,DMLOPERATION
		,INSURERTYPE
		,INSURERSEQ
		,JURISDICTION
		,STATEID
		,TIN
		,ALTID
		,NAME
		,ADDRESS1
		,ADDRESS2
		,CITY
		,STATE
		,ZIP
		,PHONENUM
		,CREATEUSERID
		,CREATEDATE
		,MODUSERID
		,MODDATE
		,FAXNUM
		,NAICCOCODE
		,NAICGPCODE
		,NCCICARRIERCODE
		,NCCIGROUPCODE
FROM src.Insurer
WHERE   ODSROWISCURRENT = 1
	AND DMLOPERATION <> 'D';


