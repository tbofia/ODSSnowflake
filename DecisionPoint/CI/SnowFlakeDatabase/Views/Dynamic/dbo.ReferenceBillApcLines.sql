CREATE OR REPLACE VIEW dbo.ReferenceBillApcLines
AS
SELECT 
		ODSPOSTINGGROUPAUDITID
		,ODSCUSTOMERID
		,ODSCREATEDATE
		,ODSSNAPSHOTDATE
		,ODSROWISCURRENT
		,ODSHASHBYTESVALUE
		,DMLOPERATION
		,BILLIDNO
		,LINE_NO
		,PAYMENTAPC
		,SERVICEINDICATOR
		,PAYMENTINDICATOR
		,OUTLIERAMOUNT
		,PRICERALLOWED
		,MEDICAREAMOUNT
FROM src.ReferenceBillApcLines
WHERE   ODSROWISCURRENT = 1
	AND DMLOPERATION <> 'D';


