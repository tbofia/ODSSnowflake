CREATE OR REPLACE VIEW dbo.Adjustor
AS
SELECT 
		ODSPOSTINGGROUPAUDITID
		,ODSCUSTOMERID
		,ODSCREATEDATE
		,ODSSNAPSHOTDATE
		,ODSROWISCURRENT
		,ODSHASHBYTESVALUE
		,DMLOPERATION
		,LADJIDNO
		,IDNUMBER
		,LASTNAME
		,FIRSTNAME
		,ADDRESS1
		,ADDRESS2
		,CITY
		,STATE
		,ZIPCODE
		,PHONE
		,FAX
		,OFFICE
		,EMAIL
		,INUSE
		,OFFICEIDNO
		,USERID
		,CREATEDATE
		,LASTCHANGEDON
FROM src.Adjustor
WHERE   ODSROWISCURRENT = 1
	AND DMLOPERATION <> 'D';


